<?php

namespace app\models;

use Yii;
use yii\data\ActiveDataProvider;
use yii\db\Query;

class TransactionSearch extends Transaction implements SearchInterface
{

    public function rules(): array
    {
        return [
            [['id', 'transaction_id', 'reference', 'phone','callback_data', 'amount', 'currency', 'payment_status', 'status', 'updated_at', 'type', 'created_at', 'description'], 'safe'],
        ];
    }

    public function search($params = null)
    {
        Yii::$app->session->remove("data");
        $query = $this->ql();
        if ($params) {
            $this->load($params);
        }


        $dataProvider = new ActiveDataProvider([
            'query' => $query,
            'pagination' => [
                'pageSize' => 15,
            ],
            'sort' => [
                'defaultOrder' => [
                    'id' => SORT_DESC,
                ],
                'attributes' => [
                    'id',
                ],
            ],
        ]);

        if (!$this->validate()) {
            return $dataProvider;
        }
        if (!empty($params['TransactionSearch'])) {
            $query->andFilterWhere(['id' => $this->id]);
            $query->andFilterWhere(['phone' => $this->phone]);
            $query->andFilterWhere(['type' => $this->type]);
            $query->andFilterWhere(['currency' => $this->currency]);
        }


        return $dataProvider;


    }

    public function ql()
    {
        return (new Query())->select(['*'])->from('transaction');
    }

    public function searchFields()
    {
        return [
            'id' => [
                'type' => 'text',
                'placeholder' => 'Search by ID',
                'name' => 'id',
            ],
            'phone' => [
                'type' => 'text',
                'placeholder' => 'Search by phone number',
                'name' => 'phone',
            ],
            'transaction_id' => [
                'type' => 'text',
                'placeholder' => 'Search by Txn no',
                'name' => 'transaction_id',
            ]
        ];
    }

    public function getData()
    {
        return Yii::$app->session->get("data") ?? $this->ql()->all();
    }

    public function ExportColumns()
    {
        return [
            'id' => 'ID',
