            'transaction_id' => 'Txn.No',
            'phone' => 'Phone',
            'amount' => ['label' => 'Amount', 'format' => function ($value) {
                return $value ? 'UGX ' . number_format($value) : '';
            }],
//            'currency' => 'Currency',
            'type' => 'Type',
