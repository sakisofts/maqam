            'phone' => 'Phone',
            'amount' => 'Amount',
            'description' => 'Description',
            'callback_data'=>'Callback Data',
            'payment_status' => 'Payment Status',
            'status' => 'Status',
            'updated_at' => 'Updated At',
            'created_at' => 'Created At',
            'type' => 'Type',
            'reference' => 'Reference',

        ];
    }

