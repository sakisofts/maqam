//                    }
//
//                    return substr($value, 0, 137) . '...';
//                }
//            ],
            'payment_status' => 'Payment Status',
            'status' => 'Status',
            'message' => 'Message',
            'updated_at' => [
                'label' => 'Settlement Date',
                'format' => function ($value) {
                    return $value ? Yii::$app->formatter->asDate($value, 'php:Y-m-d') : '';
                }
